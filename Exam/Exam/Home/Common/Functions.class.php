namespace Home\Common;

class Functions
{


    public static function auth(){
        $user = session("user");
        if (empty($user)){
            $model = M("User");
            $user = $model->where("uid = 2")->find();
            session("user", $user);
        }
        return $user;
    }

    /*
      * array unique_rand( int $min, int $max, int $num )
      * 生成一定数量的不重复随机数
      * $min 和 $max: 指定随机数的范围
      * $num: 指定生成数量
      */
    public static function unique_rand($min, $max, $num) {
        $count = 0;
        $return = array();
        while ($count < $num) {
            $return[] = mt_rand($min, $max);
            $return = array_flip(array_flip($return));
            $count = count($return);
        }
        shuffle($return);
        return $return;
    }

}